        private Texture2D ModelToTexture(int[] model)
        {
            var texture = new Texture2D(_graphicsDevice, letterWidth, letterHeight);
            texture.SetData(model.Select(i => i == 0 ? Color.Transparent : Color.White).ToArray());
            return texture;
        }

        public void DrawTextToTexture(string text, Color color, float font, float x, float y, float characterSep = 0f)
        {
            var chars = text.ToCharArray().Where(c => textures.ContainsKey(c.ToString())).ToArray();

            _spriteBatch.Begin(SpriteSortMode.Deferred, BlendState.AlphaBlend, SamplerState.PointClamp, null, null, null, null);

            for (var i = 0; i < chars.Length; i++)
            {
                _spriteBatch.Draw(textures[chars[i].ToString()],
                    new Vector2(x, y) + (font / 4f) * i * (letterWidth + characterSep) * Vector2.UnitX,
                    null, color, 0f, Vector2.Zero, font / 4f, SpriteEffects.None, 0f);
            }
            
            _spriteBatch.End();


        }
